$(function() {
    $('#bilplu').click(function() {
        var bil = Number($('#bil').html());
        bil++;
        $('#bil').html(bil);
    });
});
$(function() {
    $('#bildes').click(function() {
        var bil = Number($('#bil').html());
        bil = bil + 10;
        $('#bil').html(bil);
    });
});
$(function() {
    $('#bilmin').click(function() {
        var bil = Number($('#bil').html());
        bil--;
        $('#bil').html(bil);
    });
});




